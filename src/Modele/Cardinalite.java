package Modele;

public class Cardinalite {
    private Cardinalite min;
    private Cardinalite max;

    // Getters et Setters
    public Cardinalite getMin() {
        return min;
    }

    public void setMin(Cardinalite min) {
        this.min = min;
    }

    public Cardinalite getMax() {
        return max;
    }

    public void setMax(Cardinalite max) {
        this.max = max;
    }
}
